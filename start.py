# Router — маршрутизатор, он знает какой обработчик вызвать при каком сообщении
from aiogram import Router
# CommandStart — фильтр, который срабатывает только когда пользователь пишет /start
from aiogram.filters import CommandStart
# Message — обычное текстовое сообщение от пользователя,
# CallbackQuery — нажатие на inline-кнопку
from aiogram.types import Message, CallbackQuery
# FSMContext — «блокнот» бота для каждого пользователя,
# где хранится текущее состояние и данные игры
from aiogram.fsm.context import FSMContext

# Из config.py берём константы: количество жизней и очков за правильный ответ —
# они нужны чтобы показать правила в сообщении
from config import LIVES, POINTS
# Из states.py берём нашу группу состояний GameStates
from states import GameStates
# Из keyboards.py берём функцию, которая создаёт кнопку «Начать игру»
from keyboards import start_keyboard
# Из session.py берём функцию new_session —
# она создаёт чистое начальное состояние для новой игры
from session import new_session
# Из game.py берём функцию send_question —
# она отправляет очередной вопрос игроку
from game import send_question

# Создаём объект роутера — он будет «собирать» все обработчики этого файла
router = Router()


# Декоратор: говорим роутеру — «вызывай функцию ниже,
# когда придёт сообщение с командой /start»
@router.message(CommandStart())
# Объявляем асинхронную функцию:
# message — само сообщение от пользователя,
# state — блокнот с состоянием конкретного пользователя
async def handle_start(message, state):
    """
    Срабатывает когда пользователь пишет /start.
    Сбрасываем любое предыдущее состояние и показываем правила.
    """
    # Сбрасываем старое состояние, если игра уже шла —
    # как стереть старые записи в блокноте и начать чистый лист
    await state.clear()

    # Собираем большую строку с правилами игры;
    # скобки позволяют разбить её на несколько строчек кода для удобства чтения
    rules_text = (
        # Заголовок игры; \n\n — это два перевода строки (пустая строка между абзацами)
        "🕵️ <b>КИБЕРДЕТЕКТИВ: Распознай скам!</b>\n\n"
        # Первая строка правил
        "Тебе будут показаны сообщения из интернета.\n"
        # Вторая строка правил; <b>...</b> — жирный текст в HTML
        "Определи: это <b>СКАМ</b> или безопасное сообщение?\n\n"
        # Показываем константы из config.py;
        # f"..." — f-строка позволяет вставлять переменные прямо в текст
        f"❤️ Жизней: <b>{LIVES}</b>  |  ✨ За правильный ответ: <b>{POINTS} очков</b>\n\n"
        # Объяснение значения кнопки «Скам»
        "🚨 <b>Скам</b> — подозрительное сообщение\n"
        # Объяснение значения кнопки «Безопасно»
        "✅ <b>Безопасно</b> — обычное сообщение"
    )
    # Отправляем сообщение с правилами игроку:
    # parse_mode="HTML" — разрешаем HTML-теги (<b> и т.д.),
    # reply_markup — прикрепляем к сообщению кнопку «Начать игру»
    await message.answer(rules_text, parse_mode="HTML", reply_markup=start_keyboard())


# Декоратор: вызывай функцию ниже, когда пользователь нажал кнопку с callback_data="play_again"
# lambda — маленькая анонимная функция-фильтр: проверяет что именно эта кнопка нажата
@router.callback_query(lambda c: c.data == "play_again")
# Объявляем обработчик нажатия кнопки:
# callback — объект нажатой кнопки (содержит данные о кнопке и сообщении),
# state — блокнот с состоянием конкретного пользователя
async def handle_play_again(callback, state):
    """
    Срабатывает при нажатии «Начать игру» или «Играть снова».
    Создаём новую сессию и показываем первый вопрос.
    """
    # Убираем «часики» на кнопке — Telegram требует подтверждения
    # что мы получили нажатие, иначе кнопка будет бесконечно «крутиться»
    await callback.answer()
    # Убираем клавиатуру с предыдущего сообщения —
    # чтобы кнопка «Начать» пропала после нажатия и нельзя было нажать снова
    await callback.message.edit_reply_markup()

    # Создаём новую игровую сессию —
    # чистый словарь с нулевыми очками, полными жизнями и набором случайных вопросов
    session = new_session()
    # Сохраняем сессию в блокнот FSM —
    # теперь бот «помнит» состояние именно этого пользователя
    await state.update_data(session=session)
    # Переключаем состояние на «игра идёт» —
    # теперь бот будет ждать нажатий кнопок «Скам»/«Безопасно»
    await state.set_state(GameStates.playing)

    # Вызываем функцию из game.py — она отправит первый вопрос игроку
    await send_question(callback.message, session)
