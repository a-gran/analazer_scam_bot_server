# Модуль html нужен, чтобы безопасно показывать текст вопросов в режиме HTML
from html import escape

# Из config.py берём POINTS — очки за правильный ответ
from config import LIVES, POINTS


# Объявляем функцию;
# она возвращает два значения: строку и словарь
def process_answer(player, case, session):
    """
    Проверяет ответ игрока и обновляет сессию.

    Параметры:
        player  — ответ игрока: True (думает скам) / False (думает безопасно)
        case    — текущий вопрос (словарь с ключами text, is_scam, explanation)
        session — текущее состояние игры (lives, score, correct, ...)

    Возвращает:
        result_text — строка с результатом для отправки в Telegram (HTML-разметка)
        session     — обновлённый словарь сессии
    """
    # Сравниваем ответ игрока (True/False) с правильным ответом из вопроса (is_scam: True/False)
    if player == case["is_scam"]:
        # Если совпали — добавляем очки к счёту игрока (например, +10)
        session["score"] += POINTS
        # И увеличиваем счётчик правильных ответов на 1
        session["correct"] += 1
        # Текст заголовка для правильного ответа
        header = "✅ <b>ПРАВИЛЬНО!</b>"
    # Если ответы не совпали — игрок ошибся
    else:
        # Уменьшаем количество жизней на 1 — как в игре, когда теряешь жизнь
        session["lives"] -= 1
        # Текст заголовка для неправильного ответа
        header = "❌ <b>НЕПРАВИЛЬНО!</b>"

    # Строим строку с сердечками для визуализации жизней:
    # красные ❤️ — оставшиеся, чёрные 🖤 — потерянные;
    # умножение строки ("❤️ " * 2) повторяет её нужное количество раз
    lives_left = "❤️ " * session["lives"] + "🖤 " * (LIVES - session["lives"])
    explanation = escape(case["explanation"])

    # Формируем текст результата — он будет отправлен игроку после ответа
    result_text = (
        # Заголовок «Правильно!» или «Неправильно!» и пустая строка (\n\n)
        f"{header}\n\n"
        # Объяснение из вопроса — почему это скам или почему безопасно
        f"💬 {explanation}\n\n"
        # Текущее состояние жизней (в виде сердечек) и очков
        f"Жизни: {lives_left}  |  Очки: {session['score']}"
    )
    # Возвращаем два значения сразу:
    # текст для отправки в Telegram и обновлённую сессию с новыми очками/жизнями
    return result_text, session


# Объявляем функцию;
# принимает очки и количество вопросов, возвращает строку с «титулом»
def final_title(score, total):
    """
    Возвращает итоговое звание игрока в зависимости от результата.
    Логика полностью совпадает с оригинальным answer.py.
    """
    # Вычисляем долю набранных очков от максимально возможного:
    # максимум = total * POINTS (если правильно ответить на все вопросы)
    percent = score / (total * POINTS)
    # Если набрал 80% и больше от максимума — высший титул
    if percent >= 0.8:
        return "🏆 <b>КИБЕРДЕТЕКТИВ</b> — отличный результат!"
    # Если набрал от 50% до 79% — средний титул
    if percent >= 0.5:
        return "🛡 <b>ОСТОРОЖНЫЙ ПОЛЬЗОВАТЕЛЬ</b> — неплохо, но есть куда расти."
    # Если набрал меньше 50% — начинающий,
    # но игра уже научила распознавать некоторые признаки скама
    return "🌱 <b>НОВИЧОК</b> — не расстраивайся, теперь ты знаешь больше!"
