# Подключаем модуль random — он умеет делать случайные вещи,
# например выбирать случайные вопросы из общего списка
import random
# ИЗМЕНЕНИЕ team_2: Подключаем модуль time — он умеет считать спокойное время для таймеров
import time
# Из файла data.py берём переменную data —
# это огромный список всех вопросов (скам-кейсов)
from data import data
# Из config.py берём:
# LIVES — количество жизней в начале игры,
# COUNT — сколько вопросов выбрать для одной партии
from config import LIVES, COUNT


# Объявляем функцию new_session;
# она возвращает словарь с состоянием новой игры
def new_session():
    """
    Создаёт и возвращает словарь с начальным состоянием игры для одного игрока.

    Структура словаря:
        cases        — список из COUNT случайных вопросов для этой игры
        question_num — индекс текущего вопроса (начинаем с 0)
        lives        — количество жизней (берём из константы LIVES)
        score        — очки (начинаем с 0)
        correct      — счётчик правильных ответов (начинаем с 0)
        total        — всего вопросов в этой игре (= len(cases))
    """
    # Случайным образом выбираем COUNT штук вопросов из полного списка data —
    # каждый раз разный набор, без повторений (как вытащить карты из колоды)
    cases = random.sample(data, COUNT)
    # Возвращаем словарь — это «коробка» с именованными ячейками,
    # где хранится всё состояние одного игрока
    return {
        # Список вопросов для этой конкретной игры (случайная выборка)
        "cases": cases,
        # Индекс текущего вопроса — начинаем с нуля (первый вопрос)
        "question_num": 0,
        # Начальное количество жизней — берём из константы в config.py (обычно 3)
        "lives": LIVES,
        # Начальные очки — ноль, игрок ещё ничего не ответил
        "score": 0,
        # Счётчик правильных ответов — тоже начинаем с нуля
        "correct": 0,
        # Общее количество вопросов в этой игре — вычисляем длину списка cases
        "total": len(cases),
        # ИЗМЕНЕНИЕ team_2: Уникальная метка игровой сессии, чтобы старый таймер не мешал новой игре
        "session_id": time.monotonic(),
        # ИЗМЕНЕНИЕ team_2: Время показа текущего вопроса; сначала вопрос ещё не показан
        "question_started_at": None,
        # ИЗМЕНЕНИЕ team_2: Номер активного таймера, чтобы отличать старый вопрос от нового
        "question_token": 0,
    }


# Объявляем функцию;
# принимает сессию, возвращает словарь вопроса ИЛИ None если вопросы закончились
def get_current_case(session):
    """
    Возвращает словарь текущего вопроса или None, если вопросы закончились.

    session["question_num"] — индекс следующего вопроса, который ещё не показан.
    Если он >= total, значит все вопросы уже были показаны.
    """
    # Достаём из сессии индекс текущего вопроса
    # (например: 0 = первый, 1 = второй и т.д.)
    idx = session["question_num"]
    # Проверяем: если индекс больше или равен общему количеству вопросов —
    # все вопросы уже были показаны, новых нет
    if idx >= session["total"]:
        # Возвращаем None — специальное значение «ничего нет»,
        # сигнал что вопросов больше не осталось
        return None
    # Возвращаем словарь вопроса по индексу:
    # берём список cases и достаём элемент номер idx
    return session["cases"][idx]
