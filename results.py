# Модуль html нужен, чтобы безопасно показывать текст из настроек в режиме HTML
from html import escape

# Router — маршрутизатор, нужен чтобы подключить этот файл к диспетчеру в bot.py
from aiogram import Router
# Message — тип объекта сообщения Telegram, нужен для отправки ответа игроку
from aiogram.types import Message

# Из config.py берём список признаков скама —
# он показывается в конце игры как «шпаргалка» для игрока
from config import LIVES, SCAM_SIGNS
# Из answer.py берём функцию final_title —
# она возвращает «звание» игрока по итогам (Кибердетектив / Новичок и т.д.)
from answer import final_title
# Из keyboards.py берём функцию, создающую кнопку «Играть снова»
from keyboards import play_again_keyboard

# Создаём объект роутера — хотя в этом файле нет декораторов @router,
# роутер нужен чтобы подключить файл к диспетчеру в bot.py
router = Router()


# Объявляем асинхронную функцию:
# message — объект сообщения (куда отправлять итоги),
# session — финальное состояние игры со всеми результатами
async def show_results(message, session):
    """
    Формирует и отправляет итоговый экран после завершения игры.

    Принимает:
        message — объект сообщения для ответа
        session — финальное состояние сессии (correct, score, lives, total)
    """
    # Достаём из сессии количество правильных ответов
    correct = session["correct"]
    # Достаём из сессии итоговое количество очков
    score   = session["score"]
    # Достаём из сессии количество оставшихся жизней
    lives   = session["lives"]
    # Достаём из сессии общее количество вопросов в этой игре
    total   = session["total"]
    # ИЗМЕНЕНИЕ team_1: Считаем, сколько вопросов игрок уже прошёл до финала или кнопки «Стоп»
    passed = min(session["question_num"], total)

    # Вызываем функцию из answer.py — она посчитает процент правильных ответов
    # и вернёт текст с «титулом» игрока в зависимости от результата
    title = final_title(score, total)

    # Соединяем все признаки скама в одну строку через перенос строки;
    # каждый пункт начинается с «  • » (отступ и маркер-точка)
    signs = "\n".join(f"  • {escape(sign)}" for sign in SCAM_SIGNS)

    # Строим строку с сердечками для визуализации жизней:
    # красные ❤️ — оставшиеся жизни, чёрные 🖤 — потерянные;
    # умножение строки ("❤️ " * 2) повторяет её нужное количество раз
    lives_display = "❤️ " * lives + "🖤 " * (LIVES - lives)

    # Формируем итоговый текст — большой блок с рамкой и всеми результатами
    result_text = (
        # Верхняя граница рамки
        "━━━━━━━━━━━━━━━━━━━━━━\n"
        # Заголовок по центру; пробелы вначале — ручное центрирование текста
        "        🏁 <b>ИТОГ ИГРЫ</b>\n"
        # Нижняя граница рамки заголовка и пустая строка
        "━━━━━━━━━━━━━━━━━━━━━━\n\n"
        # Количество правильных ответов; переменные подставляются через f-строку
        f"✅ Правильных ответов: <b>{correct} из {total}</b>\n"
        # ИЗМЕНЕНИЕ team_1: Показываем прогресс, чтобы досрочный финал был понятным
        f"📌 Пройдено вопросов:   <b>{passed} из {total}</b>\n"
        # Итоговые очки
        f"⭐ Итоговый счёт:      <b>{score} очков</b>\n"
        # Оставшиеся жизни в виде сердечек
        f"❤️ Оставшиеся жизни:   {lives_display}\n\n"
        # Титул игрока (Кибердетектив / Осторожный пользователь / Новичок)
        f"{title}\n\n"
        # Заголовок перед списком признаков
        "🔍 <b>Главные признаки скама:</b>\n"
        # Сам список признаков скама, сформированный выше
        f"{signs}"
    )

    # Отправляем итоговое сообщение с HTML-разметкой и кнопкой «Играть снова»
    await message.answer(result_text, parse_mode="HTML", reply_markup=play_again_keyboard())
